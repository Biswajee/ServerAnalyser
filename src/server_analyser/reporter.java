    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        flush.doClick();
        DefaultTableModel model = (DefaultTableModel) tbl.getModel();
        try {
            //Registering JDBC Driver...
            Class.forName("com.mysql.jdbc.Driver");
            //The Database Connection Statement...
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/serverdata", "root", "1111");
            //Statement Object Command...
            Statement stmt = con.createStatement();
            String query = "";
            if (client.isSelected() == true) {
                JOptionPane.showMessageDialog(null, "Client Access log will be Displayed !");
                query = "Select * from dummy_log where optrtype='user';";
            } else if (admin.isSelected() == true) {
                JOptionPane.showMessageDialog(null, "Admin Access log will be Displayed !");
                query = "Select * from dummy_log where optrtype='admin';";
            } else {
                JOptionPane.showMessageDialog(null, "All Users Access log will be Displayed !");
                query = "Select * from dummy_log;";
            }
            ResultSet rs = stmt.executeQuery(query);

            while (rs.next()) {
                model.addRow(new Object[]{rs.getInt(1), rs.getString(2), rs.getString(3), rs.getInt(4), rs.getInt(5), rs.getDate(6), rs.getString(7)});
            }
            rs.close();
            stmt.close();
            con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Some Problem Occured !\nWe are trying to fix it Soon.");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

        private void flushActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_flushActionPerformed
            DefaultTableModel model = (DefaultTableModel) tbl.getModel();
            int rows = model.getRowCount();
            //now remove all the rows from the current table model :
            if (rows > 0) {
                for (int i = 0; i < rows; i++) {
                    model.removeRow(0);
                }
            }
}//GEN-LAST:event_flushActionPerformed

